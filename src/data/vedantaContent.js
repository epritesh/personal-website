// Function to get content for the current day
export const getTodayContent = () => {
  // Use UTC to avoid DST/timezone off-by-one errors
  const now = new Date();
  const dayOfYear = Math.floor(
    (
      Date.UTC(now.getUTCFullYear(), now.getUTCMonth(), now.getUTCDate()) -
      Date.UTC(now.getUTCFullYear(), 0, 1)
    ) / 86400000
  );

  return {
    wisdom: dailyWisdom[dayOfYear % dailyWisdom.length],
    mantra: mantras[dayOfYear % mantras.length],
    practice: practices[dayOfYear % practices.length],
    teaching: teachings[dayOfYear % teachings.length]
  };
};
